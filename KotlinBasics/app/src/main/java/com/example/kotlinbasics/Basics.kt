package com.example.kotlinbasics

fun main(){
    // val are immutable variable
    val number1 = 1
    // var are mutable variables
    // can override the data type with colon type
    var myAge: Byte = 29
    println(myAge)
    myAge = 30
    println(myAge)

    // float val
    val pi = 3.14f
    println(pi)
}